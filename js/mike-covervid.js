function randomizeVideo(){
	var sourceArray = ["videos/canada0.m4v", "videos/canada01.m4v", "videos/canada02.m4v", "videos/canada03.m4v"];
//make random number between 0 and 3
	var randomVid = Math.floor(Math.random() * 4);
	$("#randomSrc").attr("src", sourceArray[randomVid]);
}

randomizeVideo();

$('.covervid-video').coverVid(720, 1080);
